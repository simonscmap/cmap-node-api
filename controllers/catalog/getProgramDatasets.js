// :: [id] -> [Dataset]
const getAllDatasets = async (datasetIds) => {
  if (!Array.isArray (datasetIds)) {
    return ['wrong argument type']
  }

  const opt = { useNewDatasetModel: true };
  let results;
  try {
    results = await Promise.all (datasetIds.map ((id) =>
      fetchDataset ({ id }, opt)));

  } catch (e) {
    return [e];
  }

  const map = {};
  results.forEach (([err, data]) => {
    if (data) {
      map[data.ID] = data;
    }
  })

  return [null, map];

};


